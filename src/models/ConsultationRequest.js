const mongoose = require('mongoose');

const consultationRequestSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  phone: {
    type: String
  },
  topic: {
    type: String,
    required: true
  },
  problemDescription: {
    type: String,
    required: true
  },
  submittedAt: {
    type: Date,
    default: Date.now
  }
});

module.exports = mongoose.model('ConsultationRequest', consultationRequestSchema);
